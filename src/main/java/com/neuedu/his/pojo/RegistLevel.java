package com.neuedu.his.pojo;

public class RegistLevel {
    private Integer id;
    private String registCode;
    private String registName;
    private Integer registFee;
    private Integer registQuota;
    private Integer sequenceNo;
    private Integer delmark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRegistCode() {
        return registCode;
    }

    public void setRegistCode(String registCode) {
        this.registCode = registCode;
    }

    public String getRegistName() {
        return registName;
    }

    public void setRegistName(String registName) {
        this.registName = registName;
    }

    public Integer getRegistFee() {
        return registFee;
    }

    public void setRegistFee(Integer registFee) {
        this.registFee = registFee;
    }

    public Integer getRegistQuota() {
        return registQuota;
    }

    public void setRegistQuota(Integer registQuota) {
        this.registQuota = registQuota;
    }

    public Integer getSequenceNo() {
        return sequenceNo;
    }

    public void setSequenceNo(Integer sequenceNo) {
        this.sequenceNo = sequenceNo;
    }

    public Integer getDelmark() {
        return delmark;
    }

    public void setDelmark(Integer delmark) {
        this.delmark = delmark;
    }
}
